package com.liontertainment.javalearning;

public class Loops {
    public static void main(String[] args) {

        //for Loop

        int [] myNumbers = {12,15,18,21,24};

        //System.out.println(x);

        for (int i = 0; i<myNumbers.length; i++){
            int x = myNumbers[i] / 3 * 5;
            System.out.println(x);
        }





    }
}
